package ControlStatements;

public class LoopsDecrement {
	
public static void main(String[] args) {
		
		for(int var=5;var>=2;var++)
		{
			System.out.println(var+ "live tech" );
		}
		System.out.println("******");
		for(int a=-3;a>=2;a--)
		{
		System.out.println(a+"meter");
		}
		System.out.println("++++++");
		for(int i=-9;i<=6;i++)
		{
			System.out.println(i+"load runner");
		}
		System.out.println("^^^^^^");
		for(int k=-15;k>=-19;k--)
		{
			System.out.println(k+"QTP");
		}
		System.out.println("#######");
		for(int p=10;p>=-10;p=p-3)
		{
			System.out.println(p+"uff");
		}
	}

}
